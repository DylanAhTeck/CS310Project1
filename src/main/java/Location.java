
public class Location {
	String city;
	String country;
	String address2;
	String address3;
	String state;
	String address1;
	String zip_code;
	
	public Location(String city, String country, String address2, String address3, String state, String address1, String zip_code) {
		this.city = city;
		this.country = country;
		this.address2 = address2;
		this.address3 = address3;
		this.state = state;
		this.address1 = address1;
		this.zip_code = zip_code;
	}
	
}
